def set_text(p, text):
    if p.runs:
        p.runs[0].text = text
        for r in p.runs[1:]:
            r.text = ""
    else:
        p.add_run(text)


def insert_after(target_el, text, style='Normal'):
    new_p = d.add_paragraph(text, style=style)
    new_el = new_p._element
    new_el.getparent().remove(new_el)
    target_el.addnext(new_el)
    return new_el


