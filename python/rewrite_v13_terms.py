changed_runs = 0
for p in d.paragraphs:
    for run in p.runs:
        orig = run.text
        new = orig
        for old, repl in replacements:
            new = new.replace(old, repl)
        if new != orig:
            run.text = new
            changed_runs += 1

# ============ Тусгайлсан догол мөрийн солилт ============

def set_text(p, text):
    if p.runs:
        p.runs[0].text = text
        for r in p.runs[1:]:
            r.text = ""
    else:
        p.add_run(text)

