def paragraph_text(p: ET.Element) -> str:
    parts: list[str] = []
    for node in p.iter():
        if node.tag in (W + "t", M + "t") and node.text:
            parts.append(node.text)
    return "".join(parts).strip()


def collect_nonempty_paragraphs(root: ET.Element) -> list[tuple[ET.Element, str]]:
    out: list[tuple[ET.Element, str]] = []
    for p in root.iter(W + "p"):
        text = paragraph_text(p)
        if text:
            out.append((p, text))
    return out


def clone(el: ET.Element) -> ET.Element:
    return ET.fromstring(ET.tostring(el))


def set_p_text(p: ET.Element, text: str) -> None:
    p_pr = p.find(W + "pPr")
    first_rpr = None
    for r in p.findall(W + "r"):
        rpr = r.find(W + "rPr")
        if rpr is not None:
            first_rpr = clone(rpr)
            break
    for child in list(p):
        p.remove(child)
    if p_pr is not None:
        p.append(clone(p_pr))
    r = ET.SubElement(p, W + "r")
    if first_rpr is not None:
        r.append(first_rpr)
    t = ET.SubElement(r, W + "t")
    t.set(XML + "space", "preserve")
    t.text = text


def find_table_containing(root: ET.Element, marker: str) -> ET.Element | None:
    for tbl in root.iter(W + "tbl"):
        if marker in paragraph_text(tbl):
            return tbl
    return None


def remove_last_column(tbl: ET.Element | None) -> None:
    if tbl is None:
        print("warning: table for column removal not found")
        return
    grid = tbl.find(W + "tblGrid")
    if grid is not None:
        cols = grid.findall(W + "gridCol")
        if len(cols) > 1:
            grid.remove(cols[-1])
    for tr in tbl.findall(W + "tr"):
        cells = tr.findall(W + "tc")
        if len(cells) > 1:
            tr.remove(cells[-1])


def ensure_child(parent: ET.Element, tag: str) -> ET.Element:
    child = parent.find(tag)
    if child is None:
        child = ET.SubElement(parent, tag)
    return child


def set_w_attr(el: ET.Element, name: str, value: str) -> None:
    el.set(W + name, value)


def update_styles_xml(styles_bytes: bytes) -> bytes:
    root = ET.fromstring(styles_bytes)
    normal = None
    for style in root.findall(W + "style"):
        if style.get(W + "styleId") == "Normal":
            normal = style
            break
    if normal is None:
        normal = ET.SubElement(root, W + "style")
        set_w_attr(normal, "type", "paragraph")
        set_w_attr(normal, "default", "1")
        set_w_attr(normal, "styleId", "Normal")
        name = ET.SubElement(normal, W + "name")
        set_w_attr(name, "val", "Normal")

    ppr = ensure_child(normal, W + "pPr")
    spacing = ensure_child(ppr, W + "spacing")
    set_w_attr(spacing, "before", "120")
    set_w_attr(spacing, "after", "120")
    set_w_attr(spacing, "line", "360")
    set_w_attr(spacing, "lineRule", "auto")
    jc = ensure_child(ppr, W + "jc")
    set_w_attr(jc, "val", "both")

    rpr = ensure_child(normal, W + "rPr")
    rfonts = ensure_child(rpr, W + "rFonts")
    for attr in ("ascii", "hAnsi", "eastAsia", "cs"):
        set_w_attr(rfonts, attr, "Times New Roman")
    sz = ensure_child(rpr, W + "sz")
    set_w_attr(sz, "val", "24")
    szcs = ensure_child(rpr, W + "szCs")
    set_w_attr(szcs, "val", "24")
    return ET.tostring(root, encoding="utf-8", xml_declaration=True)


def update_settings_xml(settings_bytes: bytes) -> bytes:
    root = ET.fromstring(settings_bytes)
    update_fields = root.find(W + "updateFields")
    if update_fields is None:
        update_fields = ET.SubElement(root, W + "updateFields")
    set_w_attr(update_fields, "val", "true")
    return ET.tostring(root, encoding="utf-8", xml_declaration=True)


def relationship_media_map(rels_bytes: bytes) -> dict[str, str]:
    root = ET.fromstring(rels_bytes)
    out: dict[str, str] = {}
    for rel in root.findall(REL + "Relationship"):
        rid = rel.get("Id")
        target = rel.get("Target")
        if rid and target and target.startswith("media/"):
            out[rid] = "word/" + target
    return out


def adjust_image_extents(root: ET.Element, rels_bytes: bytes, image_map: dict[str, Path]) -> None:
    rid_to_media = relationship_media_map(rels_bytes)
    display_width = 5_486_400  # 6 inches; fits inside 30/20 mm margins.
    for inline in root.iter(WP + "inline"):
        blip = inline.find(".//" + A + "blip")
        if blip is None:
            continue
        rid = blip.get(R + "embed")
        media_name = rid_to_media.get(rid or "")
        if media_name not in image_map:
            continue
        with Image.open(image_map[media_name]) as im:
            width_px, height_px = im.size
        if not width_px or not height_px:
            continue
        display_height = int(display_width * height_px / width_px)
        extent = inline.find(WP + "extent")
        if extent is not None:
            extent.set("cx", str(display_width))
            extent.set("cy", str(display_height))
        graphic_ext = inline.find(".//" + A + "xfrm/" + A + "ext")
        if graphic_ext is not None:
            graphic_ext.set("cx", str(display_width))
            graphic_ext.set("cy", str(display_height))


